# 3. 合并真实数据 + 投影数据
# ============================================================
def get_series(ccode, key):
    """合并WB真实数据 + UN投影数据"""
    d = pop_data.get(ccode, {}).get(key, {})
    proj = UN_PROJECTIONS.get(ccode, {})

    combined = {}
    # 真实数据 (WB, up to 2024)
    for yr_str, val in d.items():
        yr = int(yr_str)
        if yr <= 2024:
            combined[yr] = val

    # 投影数据 (UN WPP 2024 Medium Variant)
    for yr, vals in proj.items():
        if yr > 2024:
            if key == "pop_total":
                combined[yr] = vals[0]
            elif key == "pop_0_14":
                combined[yr] = vals[1]
            elif key == "pop_15_64":
                combined[yr] = vals[2]
            elif key == "pop_65up":
                combined[yr] = vals[3]
            elif key == "fertility":
                combined[yr] = vals[4]
            elif key == "urban":
                combined[yr] = vals[5]

    return sorted(combined.items())


